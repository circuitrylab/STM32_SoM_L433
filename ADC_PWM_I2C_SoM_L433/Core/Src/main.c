void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
	// If interrupt is generated by ADC1 proceed with calculations
	if(hadc->Instance == ADC1) {
		// Get ADC raw value.
		adcRaw = HAL_ADC_GetValue(hadc);

		/*
		 * Map ADC reading to the scales of the PWM signal duty cycle. Since the
		 * period for timer 1 (PWM timer) was set at 1000. The duty cycle range from 0 to 1000,
		 * which translate to 0-100% (for a direct conversion you could select a period of 100).
		 */
		dutyCycle = mapValue(adcRaw, ADC_MIN, ADC_MAX, PWM_DC_MIN, PWM_DC_MAX);

		// Set the new duty cycle.
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, dutyCycle);
	}
}

uint16_t mapValue(uint16_t value, uint16_t inMin, uint16_t inMax, uint16_t outMin, uint16_t outMax)
{
	static volatile uint16_t convertedValue = 0;

	convertedValue = (((value - inMin) * (outMax - outMin)) / (inMax - inMin)) + inMin;

	return convertedValue;
}
